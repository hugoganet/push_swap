#include "libft.h"

/**
 * @brief Copies n bytes from memory area src to memory area dest.
 * 				The memory areas may overlap.
 * @param void* dest
 * @param const void* src
 * @param size_t n
 * @return void* dest
 */
void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (dest < src)
	{
		i = 0;
		while (i < n)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
	}
	else if (dest > src)
	{
		i = n;
		while (i > 0)
		{
			i--;
			((char *)dest)[i] = ((char *)src)[i];
		}
	}
	return ((char *)dest);
}
